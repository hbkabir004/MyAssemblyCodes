MODEL SMALL
.STACK 100H
.DATA
A DB 1,2,3,4,5 ;ARRAY INPUT
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV AL,0
MOV CX,4 ;COUNTER REGISTER - STARTING FROM 4 TO 0
MOV DI,0 ;DESTINATION INDEX REGISTER
SUM:
MOV AL,A[DI]
ADD AL,A[DI+1]
MOV AH,2
ADD AL,30H
MOV DL,AL
INT 21H
MOV AH,2
MOV DL,' '
INT 21H
INC DI ;INCREAMENT DI
DEC CX
CMP CX,0
JNE SUM