; WRITING AN ASSEMBLY PROGRAM TO READ ONE OF THE HEX DIGIT, A-F AND DISPLAY IT ON THE NEXT LINE IN DECIMAL

.MODEL SMALL
.STACK 100H

.DATA    ; DATA SEGMENT
MSG1 DB 'ENTER A HEX NUMBER: $'
MSG2 DB 0DH, 0AH,'IN DECIMAL IT IS:  $'
A DB ?

.CODE    ; CODE SEGMENT
MAIN PROC
MOV AX,@DATA
MOV DS, AX

LEA DX,MSG1      ; PRINT MSG1
MOV AH,9
INT 21H

MOV AH,1        ; CHARACTER INPUT
INT 21H
MOV A,AL


SUB A,11H        ; FUNCTION CALCULATION


LEA DX,MSG2      ; PRINT MSG2
MOV AH,9
INT 21H

MOV AH,2        ; CHARACTER OUTPUT
MOV DL,31H
INT 21H
MOV AH,2
MOV DL,A
INT 21H


MOV AH, 4CH
INT 21H

MAIN ENDP
END MAIN

